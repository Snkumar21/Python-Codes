# Example of printing month by name in python...

month = int(input("Enter the month :"))

if month == 1:
    print("Jan")
elif month == 2:
    print("Feb")
elif month == 3:
    print("March")
elif month == 4:
    print("April")
elif month == 5:
    print("May")
elif month == 6:
    print("June")
elif month == 7:
    print("July")
elif month == 8:
    print("August")
elif month == 9:
    print("September")
elif month == 10:
    print("Oct")
elif month == 11:
    print("Nov")
elif month == 12:
    print("Dec")
else:
    print("Invalid input")
