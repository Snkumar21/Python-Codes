#Assignment Operator

print("Assignment Operators")
a = 10
print("Initial value of a:", a)

a += 5  # Same as a = a + 5
print("After a += 5, a =", a)

a -= 3  # Same as a = a - 3
print("After a -= 3, a =", a)

a *= 2  # Same as a = a * 2
print("After a *= 2, a =", a)

a /= 2  # Same as a = a / 2
print("After a /= 2, a =", a)

a %= 3  # Same as a = a % 3
print("After a %= 3, a =", a)

a **= 2  # Same as a = a ** 2
print("After a **= 2, a =", a)

a //= 2  # Same as a = a // 2
print("After a //= 2, a =", a)

print()